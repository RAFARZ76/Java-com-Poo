
public class Exemplo05 {

	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		int res;
		
		//Soma
		 res = a + b;
		 System.out.println("SOMA: " + res);
		 
		//Subtração
		 res = a - b;
		 System.out.println("SUBT: " + res);
		 
		//Multiplicação
		 res = a * b;
		 System.out.println("MULT: " + res);
		 
		//Soma
		 res = a / b;
		 System.out.println("DIVS: " + res);
	

	}

}
